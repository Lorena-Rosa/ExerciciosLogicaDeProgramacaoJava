package exerciciosFaccat;

//A jornada de trabalho semanal de um funcionário é de 40 horas. O funcionário que trabalhar mais
//de 40 horas receberá hora extra, cujo cálculo é o valor da hora regular com um acréscimo de 50%.
//Escreva um algoritmo que leia o número de horas trabalhadas em um mês, o salário por hora e escreva
//o salário total do funcionário, que deverá ser acrescido das horas extras, caso tenham sido trabalhadas
//(considere que o mês possua 4 semanas exatas).

import java.util.Scanner;

public class Exercicio22JornadaTrabalho {
 public static void main(String[] args) {
     Scanner scanner = new Scanner(System.in);

     System.out.print("Quantas horas o funcionário trabalhou no mês? ");
     int horasTrabalhadas = scanner.nextInt();

     System.out.print("Qual o salário por hora? ");
     double salarioHora = scanner.nextDouble();

     double salarioTotal;

     if (horasTrabalhadas < 161) {
         salarioTotal = horasTrabalhadas * salarioHora;
     } else {
         salarioTotal = 160 * salarioHora + (horasTrabalhadas - 160) * (salarioHora * 1.5);
     }

     System.out.printf("O salário total é de: R$%.2f\n", salarioTotal);

     scanner.close();
 }
}